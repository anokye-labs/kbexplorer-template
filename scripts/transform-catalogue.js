/**
 * Transform a kb-architect JSON catalogue into kbexplorer content files.
 *
 * Input:  JSON catalogue (from kb-architect agent or stdin)
 * Output: content/config.yaml + content/{slug}.md skeleton files
 *
 * Usage:
 *   node scripts/transform-catalogue.js <catalogue.json>
 *   cat catalogue.json | node scripts/transform-catalogue.js
 */


#!/usr/bin/env node

/**
 * Transform a kb-architect JSON catalogue into kbexplorer content files.
 *
 * Input:  JSON catalogue (from kb-architect agent or stdin)
 * Output: content/config.yaml + content/{slug}.md skeleton files
 *
 * Usage:
 *   node scripts/transform-catalogue.js <catalogue.json>
 *   cat catalogue.json | node scripts/transform-catalogue.js
 */

import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  readFileSync,
  writeFileSync,
  mkdirSync,
  existsSync,
} from 'node:fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');

// ── Emoji Defaults ─────────────────────────────────────────

const TOPIC_EMOJI_MAP = {
  architecture: '🏗️',
  overview: '🏗️',
  data: '💾',
  database: '💾',
  state: '💾',
  api: '🔌',
  network: '🔌',
  http: '🔌',
  ui: '🎨',
  component: '🎨',
  view: '🎨',
  frontend: '🎨',
  auth: '🔐',
  security: '🔐',
  config: '⚙️',
  build: '⚙️',
  deploy: '⚙️',
  infra: '⚙️',
  test: '🧪',
  testing: '🧪',
  engine: '⚡',
  core: '⚡',
  logic: '⚡',
  docs: '📖',
  guide: '📖',
  documentation: '📖',
  cli: '🔧',
  tool: '🔧',
  script: '🔧',
  graph: '🕸️',
  visual: '🎭',
  theme: '🎨',
  cache: '📦',
  performance: '⚡',
};

function inferEmoji(title, cluster) {
  const lower = `${title} ${cluster}`.toLowerCase();
  for (const [keyword, emoji] of Object.entries(TOPIC_EMOJI_MAP)) {
    if (lower.includes(keyword)) return emoji;
  }
  return '📄';
}

// ── Colour Palette ─────────────────────────────────────────

const CLUSTER_COLORS = [
  '#4A9CC8', '#8CB050', '#E8A838', '#C07840',
  '#D4A050', '#5A98A8', '#9A8A78', '#C04040',
  '#A86FDF', '#39FF14', '#FF6B6B', '#4ECDC4',
];

// ── Transform ──────────────────────────────────────────────

export function transformCatalogue(catalogue, outputDir = resolve(projectRoot, 'content')) {
  const { clusters = {}, nodes = [] } = catalogue;

  // Assign colors to clusters if not present
  let colorIdx = 0;
  for (const [id, cluster] of Object.entries(clusters)) {
    if (!cluster.color) {
      cluster.color = CLUSTER_COLORS[colorIdx % CLUSTER_COLORS.length];
      colorIdx++;
    }
  }

  // Generate config.yaml — content-only (title + clusters)
  // Visual/theme/feature settings use app defaults and aren't part of content generation
  const clusterYaml = Object.entries(clusters)
    .map(([id, c]) => `  ${id}:\n    name: "${c.name}"\n    color: "${c.color}"`)
    .join('\n');

  const configYaml = `title: "${catalogue.title || 'Knowledge Base'}"
subtitle: "${catalogue.subtitle || 'Generated by kbexplorer'}"

clusters:
${clusterYaml}
`;

  mkdirSync(outputDir, { recursive: true });
  const configPath = resolve(outputDir, 'config.yaml');
  writeFileSync(configPath, configYaml, 'utf-8');
  console.log(`✓ Written ${configPath}`);

  // Generate skeleton .md files — flat in content/, graph structure comes from frontmatter
  let filesWritten = 0;
  for (const node of nodes) {
    const emoji = node.emoji || inferEmoji(node.title, node.cluster);
    const connections = (node.connections || [])
      .map(c => `  - to: "${c.to}"\n    description: "${c.description}"`)
      .join('\n');

    const frontmatter = [
      '---',
      `id: "${node.id}"`,
      `title: "${node.title}"`,
      `emoji: "${emoji}"`,
      `cluster: ${node.cluster}`,
    ];
    if (node.parent) {
      frontmatter.push(`parent: "${node.parent}"`);
    }
    if (connections) {
      frontmatter.push(`connections:\n${connections}`);
    } else {
      frontmatter.push('connections: []');
    }
    frontmatter.push('---');

    const body = node.prompt
      ? `\n# ${node.title}\n\n<!-- kb-writer prompt: ${node.prompt} -->\n\n_Content to be generated by kb-writer agent._\n`
      : `\n# ${node.title}\n\n_Content to be generated by kb-writer agent._\n`;

    const filePath = resolve(outputDir, `${node.id}.md`);

    if (!existsSync(filePath)) {
      writeFileSync(filePath, frontmatter.join('\n') + '\n' + body, 'utf-8');
      filesWritten++;
    } else {
      console.log(`  ⏭ ${node.id}.md already exists — skipping`);
    }
  }

  console.log(`✓ Generated ${filesWritten} skeleton files in ${outputDir}`);
  return { configPath, filesWritten, totalNodes: nodes.length };
}

// ── CLI Entry Point ────────────────────────────────────────

async function main() {
  let input;

  if (process.argv[2]) {
    // Read from file argument
    const filePath = resolve(process.cwd(), process.argv[2]);
    input = readFileSync(filePath, 'utf-8');
  } else if (!process.stdin.isTTY) {
    // Read from stdin
    const chunks = [];
    for await (const chunk of process.stdin) {
      chunks.push(chunk);
    }
    input = Buffer.concat(chunks).toString('utf-8');
  } else {
    console.error('Usage: node transform-catalogue.js <catalogue.json>');
    console.error('       cat catalogue.json | node transform-catalogue.js');
    process.exit(1);
  }

  const catalogue = JSON.parse(input);
  const outputDir = process.argv[3]
    ? resolve(process.cwd(), process.argv[3])
    : resolve(projectRoot, 'content');

  transformCatalogue(catalogue, outputDir);
}

main().catch(err => {
  console.error('Error:', err.message);
  process.exit(1);
});
